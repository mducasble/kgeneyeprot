// ─── 1. Symlink node_modules/expo-kgen-advanced-capture ───────────────────────
// Uses a RELATIVE target so the symlink works on any machine regardless of
// absolute project path (critical for git portability).

const symlinkDest = path.join(ROOT, "node_modules", "expo-kgen-advanced-capture");
// Relative path from node_modules/ to modules/expo-kgen-advanced-capture
const symlinkRelTarget = path.join("..", "modules", "expo-kgen-advanced-capture");

try {
  const stat = fs.lstatSync(symlinkDest);
  if (stat.isSymbolicLink()) {
    const current = fs.readlinkSync(symlinkDest);
    if (current !== symlinkRelTarget) {
      fs.unlinkSync(symlinkDest);
      fs.symlinkSync(symlinkRelTarget, symlinkDest, "dir");
      console.log(`re-created symlink: node_modules/expo-kgen-advanced-capture -> ${symlinkRelTarget}`);
    } else {
      console.log("symlink ok: node_modules/expo-kgen-advanced-capture");
    }
  } else {
    console.log("[ExpoKgen] node_modules/expo-kgen-advanced-capture exists but is not a symlink — skipping");
  }
} catch (_e) {
  // lstatSync threw → symlink doesn't exist yet
  try {
    fs.symlinkSync(symlinkRelTarget, symlinkDest, "dir");
    console.log(`created symlink: node_modules/expo-kgen-advanced-capture -> ${symlinkRelTarget}`);
  } catch (e) {
    console.warn("[ExpoKgen] Could not create symlink:", e.message);
  }
}

// ─── 2. Inject pod + post_install hook into Podfile ───────────────────────────

const podfilePath = path.join(ROOT, "ios", "Podfile");
const podLine = "  pod 'ExpoKgenAdvancedCapture', :path => '../modules/expo-kgen-advanced-capture'";

if (fs.existsSync(podfilePath)) {
  let podfile = fs.readFileSync(podfilePath, "utf8");
  let changed = false;

  // Add pod declaration if missing
  if (!podfile.includes("ExpoKgenAdvancedCapture")) {
    podfile = podfile.replace("use_expo_modules!", `use_expo_modules!\n${podLine}`);
    changed = true;
    console.log("patched: ios/Podfile — added ExpoKgenAdvancedCapture pod");
  }

  // Remove any stale post_install hooks from previous script versions
  const brokenHookPattern = /\n\s*system\("cd \.\. && node scripts\/fix-ios-build\.js --patch-provider-only"\)\n/g;
  if (brokenHookPattern.test(podfile)) {
    podfile = podfile.replace(brokenHookPattern, "\n");
    changed = true;
    console.log("patched: ios/Podfile — removed stale post_install hook");
  }

  // Remove any standalone auto-generated post_install block we may have added
  const autoBlockPattern = /\n# Auto-patch ExpoModulesProvider\.swift[\s\S]*?^end\n/m;
  if (autoBlockPattern.test(podfile)) {
    podfile = podfile.replace(autoBlockPattern, "\n");
    changed = true;
    console.log("patched: ios/Podfile — removed auto-generated post_install block");
  }

  if (changed) {
    fs.writeFileSync(podfilePath, podfile, "utf8");
  }
}

// ─── 3. Direct ExpoModulesProvider.swift patch ───────────────────────────────
// Run AFTER `pod install` if the symlink approach did not auto-register the module:
//   node scripts/fix-ios-build.js --patch-provider
//
// ExpoModulesProvider.swift is generated by CocoaPods into:
//   ios/Pods/Target Support Files/<AppTarget>/ExpoModulesProvider.swift
// The search below covers that location AND any location inside ios/.

const patchProvider = process.argv.includes("--patch-provider") ||
                      process.argv.includes("--patch-provider-only");

if (patchProvider) {
  const iosDir = path.join(ROOT, "ios");
  if (!fs.existsSync(iosDir)) {
    console.log("[ExpoKgen] ios/ not found — run this on the Mac after `pod install`.");
    process.exit(0);
  }

  // Search for ExpoModulesProvider.swift everywhere under ios/
  // (including Pods/Target Support Files/… which is the generated location)
  function findProvider(dir, depth = 0) {
    if (depth > 8) return null;
    try {
      const entries = fs.readdirSync(dir, { withFileTypes: true });
      for (const entry of entries) {
        if (entry.name === "ExpoModulesProvider.swift") {
          return path.join(dir, entry.name);
        }
        if (
          entry.isDirectory() &&
          !entry.name.startsWith(".") &&
          entry.name !== "build" &&
          entry.name !== "DerivedData"
        ) {
          const found = findProvider(path.join(dir, entry.name), depth + 1);
          if (found) return found;
        }
      }
    } catch {}
    return null;
  }

  const providerPath = findProvider(iosDir);

  if (!providerPath) {
    console.log("[ExpoKgen] ExpoModulesProvider.swift not found. Contents of ios/:");
    try {
      fs.readdirSync(iosDir).forEach((f) => console.log("  ", f));
    } catch {}
    console.log("[ExpoKgen] Run 'cd ios && pod install' first, then run this script again.");
    process.exit(1);
  }

  console.log("[ExpoKgen] Found ExpoModulesProvider.swift at:", providerPath);

  const content = fs.readFileSync(providerPath, "utf8");

  if (content.includes("ExpoKgenAdvancedCaptureModule")) {
    console.log("[ExpoKgen] ✅ ExpoModulesProvider.swift already contains ExpoKgenAdvancedCaptureModule");
    process.exit(0);
  }

  console.log("[ExpoKgen] Current provider file content:");
  console.log("─".repeat(60));
  console.log(content);
  console.log("─".repeat(60));

  let patched = null;

  // Pattern 1: ModuleName.self, (with trailing comma — most entries)
  const selfComma = /^(\s+)(\w+Module\.self,)/m;
  // Pattern 2: ModuleName.self (last entry, no comma — before ] or ))
  const selfNoComma = /^(\s+)(\w+Module\.self)\s*$/m;
  // Pattern 3: ModuleName(), (instance with comma)
  const instanceComma = /^(\s+)(\w+\(\),)/m;
  // Pattern 4: Any .self, (non-Module suffix)
  const anySelfComma = /^(\s+)(\w+\.self,)/m;
  // Pattern 5: return [ ... ] — inject into the array
  const returnArray = /^(\s*)(return\s*\[)\s*$/m;

  const m1 = content.match(selfComma);
  const m2 = content.match(selfNoComma);
  const m3 = content.match(instanceComma);
  const m4 = content.match(anySelfComma);
  const m5 = content.match(returnArray);

  if (m1) {
    const indent = m1[1];
    patched = content.replace(selfComma, `${indent}ExpoKgenAdvancedCaptureModule.self,\n${indent}${m1[2]}`);
    console.log("[ExpoKgen] Matched pattern: ModuleName.self, (with comma)");
  } else if (m2) {
    const indent = m2[1];
    patched = content.replace(selfNoComma, `${indent}ExpoKgenAdvancedCaptureModule.self,\n${indent}${m2[2]}`);
    console.log("[ExpoKgen] Matched pattern: ModuleName.self (no comma, last entry)");
  } else if (m3) {
    const indent = m3[1];
    patched = content.replace(instanceComma, `${indent}ExpoKgenAdvancedCaptureModule(),\n${indent}${m3[2]}`);
    console.log("[ExpoKgen] Matched pattern: ModuleName(), (instance)");
  } else if (m4) {
    const indent = m4[1];
    patched = content.replace(anySelfComma, `${indent}ExpoKgenAdvancedCaptureModule.self,\n${indent}${m4[2]}`);
    console.log("[ExpoKgen] Matched pattern: AnyName.self, (generic)");
  } else if (m5) {
    const indent = m5[1];
    patched = content.replace(returnArray, `${indent}${m5[2]}\n${indent}      ExpoKgenAdvancedCaptureModule.self,`);
    console.log("[ExpoKgen] Matched pattern: return [ (injected after opening bracket)");
  }

  if (patched) {
    // Add "import ExpoKgenAdvancedCapture" if missing — without this import,
    // the Swift compiler cannot resolve the ExpoKgenAdvancedCaptureModule class
    if (!patched.includes("import ExpoKgenAdvancedCapture")) {
      // Insert after the last existing import statement
      const lastImportMatch = patched.match(/^(import\s+\w+)\s*$/gm);
      if (lastImportMatch && lastImportMatch.length > 0) {
        const lastImport = lastImportMatch[lastImportMatch.length - 1];
        patched = patched.replace(lastImport, `${lastImport}\nimport ExpoKgenAdvancedCapture`);
        console.log("[ExpoKgen] Added: import ExpoKgenAdvancedCapture");
      } else {
        // Fallback: add at the very top
        patched = `import ExpoKgenAdvancedCapture\n${patched}`;
        console.log("[ExpoKgen] Added: import ExpoKgenAdvancedCapture (at top)");
      }
    }

    fs.writeFileSync(providerPath, patched, "utf8");
    console.log("[ExpoKgen] ✅ ExpoModulesProvider.swift patched successfully");
    console.log("[ExpoKgen]    Path:", providerPath);
    console.log("[ExpoKgen]    → Clean Xcode build required: ⌘⇧K then ⌘R");

    // Show the patched file for verification
    console.log("[ExpoKgen] Patched file content:");
    console.log("─".repeat(60));
    console.log(fs.readFileSync(providerPath, "utf8"));
    console.log("─".repeat(60));
  } else {
    console.log("[ExpoKgen] ⚠️ Could not auto-patch — no known pattern matched.");
    console.log("[ExpoKgen]    Path:", providerPath);
    console.log("");
    console.log("[ExpoKgen]    Please edit the file manually:");
    console.log("[ExpoKgen]    1. Add this line at the top with the other imports:");
    console.log("[ExpoKgen]       import ExpoKgenAdvancedCapture");
    console.log("[ExpoKgen]    2. Add this line inside the module array:");
    console.log("[ExpoKgen]       ExpoKgenAdvancedCaptureModule.self,");
  }
}
